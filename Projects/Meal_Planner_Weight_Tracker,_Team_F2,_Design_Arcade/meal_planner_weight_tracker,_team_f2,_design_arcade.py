def get_user_tracker_records(trackerData, target_username):
    heights = []
    weights = []

    for entry in trackerData:
        if entry[0] == target_username:
            try:
                h = float(entry[1])
                w = float(entry[2])
                heights.append(h)
                weights.append(w)
            except ValueError:
                pass  # Skip invalid numeric data
    return heights, weights

